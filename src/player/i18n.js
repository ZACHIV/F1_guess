export function detectInitialLocale() {
  if (typeof window !== 'undefined') {
    const stored = window.localStorage.getItem('f1_guess_locale');
    if (stored) {
      return normalizeLocale(stored);
    }
  }

  if (typeof navigator !== 'undefined') {
    return normalizeLocale(navigator.language || 'en');
  }

  return 'en';
}

export function getLanguageBadge(locale) {
  return getLocaleLabel(locale);
}

export function t(locale, key, variables = {}) {
  const normalizedLocale = normalizeLocale(locale);
  const template = MESSAGES[normalizedLocale]?.[key] ?? MESSAGES.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, token) =>
    variables[token] == null ? '' : String(variables[token]));
}

export function getLocalizedAnswerLabel(challenge, locale) {
  const normalizedLocale = normalizeLocale(locale);
  const track = getTrackNameByLocale(challenge?.trackName || '', normalizedLocale);
  const country = getCountryNameByLocale(challenge?.trackCountry || '', normalizedLocale);
  return `${track} · ${country}`;
}
