export function normalizeLocale(locale) {
  const value = String(locale ?? '').trim();
  if (!value) {
    return 'en';
  }

  if (value === 'zh') {
    return 'zh-Hans';
  }

  const lower = value.toLowerCase();
  if (lower.startsWith('zh-tw') || lower.startsWith('zh-hk') || lower.startsWith('zh-mo') || lower === 'zh-hant') {
    return 'zh-Hant';
  }
  if (lower.startsWith('zh')) {
    return 'zh-Hans';
  }
  if (lower.startsWith('fr')) {
    return 'fr';
  }
  if (lower.startsWith('es')) {
    return 'es';
  }
  if (lower.startsWith('it')) {
    return 'it';
  }
  if (lower.startsWith('ja')) {
    return 'ja';
  }
  if (lower.startsWith('de')) {
    return 'de';
  }
  if (lower.startsWith('ko')) {
    return 'ko';
  }

  return SUPPORTED_LOCALES.includes(value) ? value : 'en';
}

export function getLocaleLabel(locale) {
  return LOCALE_LABELS[normalizeLocale(locale)] ?? LOCALE_LABELS.en;
}

export function getTrackNameByLocale(trackName, locale) {
  const normalizedLocale = normalizeLocale(locale);
  return TRACK_NAME_TRANSLATIONS[trackName]?.[normalizedLocale] || trackName;
}

export function getCountryNameByLocale(countryName, locale) {
  const normalizedLocale = normalizeLocale(locale);
  return COUNTRY_NAME_TRANSLATIONS[countryName]?.[normalizedLocale] || countryName;
}

export function getAllLocalizedTrackNames(trackName) {
  const localizedNames = SUPPORTED_LOCALES
    .map((locale) => getTrackNameByLocale(trackName, locale))
    .filter(Boolean);
  return [...new Set([trackName, ...localizedNames])];
}

export function getAllLocalizedCountryNames(countryName) {
  const localizedNames = SUPPORTED_LOCALES
    .map((locale) => getCountryNameByLocale(countryName, locale))
    .filter(Boolean);
  return [...new Set([countryName, ...localizedNames])];
}
